    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
       
        if(!txtidcubiculo.getText().equals("")){
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¿Estás seguro de eliminar el cubículo?", "Confirmar", 2);
            
            if(confirmacion == 0){
                Fcubiculo funcion = new Fcubiculo();
                Vcubiculo dts = new Vcubiculo();
                
                dts.setIdcubiculo(Integer.parseInt(txtidcubiculo.getText()));
                funcion.eliminar(dts);
                mostrar("");
                inhabilitar(); //para dejar desactivadas todas las cajitas
            }
        }
        
    }//GEN-LAST:event_btneliminarActionPerformed

    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

        //validamos que todos los campos esten llenos
        if (txtnumero.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un número de cubículo");
            txtnumero.requestFocus();
            return;
        }
        if (txtdescripcion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una descripción para el cubículo");
            txtdescripcion.requestFocus();
            return;
        }
        if (txtpreciobase.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un precio base para el cubículo");
            txtpreciobase.requestFocus();
            return;
        }
        if (txtcaracteristicas.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar las características del cubículo");
            txtcaracteristicas.requestFocus();
            return;
        }

        Vcubiculo dts = new Vcubiculo();
        Fcubiculo funcion = new Fcubiculo();

        dts.setNumero(txtnumero.getText());

        int seleccionado = cdopiso.getSelectedIndex();
        dts.setPiso((String) cdopiso.getItemAt(seleccionado)); //el string es para convertirlo
        
        dts.setDescripcion(txtdescripcion.getText());
        dts.setCaracteristicas(txtcaracteristicas.getText());
        dts.setPrecio_base(Double.parseDouble(txtpreciobase.getText()));
        
        seleccionado = cdoestado.getSelectedIndex();
        dts.setEstado((String) cdoestado.getItemAt(seleccionado));
        
        seleccionado = cdotipocubiculo.getSelectedIndex();
        dts.setTipo_cubiculo((String) cdotipocubiculo.getItemAt(seleccionado));
        
        if(accion.equals("guardar")){
            if(funcion.insertar(dts)){
                JOptionPane.showMessageDialog(rootPane, "El cubículo fue registrado de manera exitosa.");
                mostrar(""); //vacia para que muestre todos los registros
                inhabilitar();
            }
        }else if(accion.equals("editar")){
            dts.setIdcubiculo(Integer.parseInt(txtidcubiculo.getText()));
            
            if(funcion.editar(dts)){
                JOptionPane.showMessageDialog(rootPane, "El cubículo fue editado de manera exitosa.");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

